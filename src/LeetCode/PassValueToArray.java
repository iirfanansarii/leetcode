package LeetCode;

public class PassValueToArray {
    public static void main(String[] args) {
        int marks[] = {10, 20, 30, 40, 50};
        display(marks);
    }

    public static void display(int[] result) {
        for (int i = 0; i < result.length; i++){
            System.out.println(result[i]);

    }
}
}